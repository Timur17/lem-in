#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;

	str = s1;
	while (*str)
		str++;
	while (*s2 && n-- > 0)
		*str++ = *s2++;
	*str = '\0';
	return (s1);
}
